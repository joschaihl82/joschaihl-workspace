/* --- 4. Utility & Graphics --- */
void* memset(void* dest, int ch, size_t count) {
    uint8_t* p = (uint8_t*)dest;
    while (count--) *p++ = (uint8_t)ch;
    return dest;
}

/* kernel_alloc_page checks for exhaustion and returns NULL on failure (no printf here) */
void* kernel_alloc_page() {
    if (heap_ptr + 4096 > HEAP_SIZE) {
        return NULL;
    }
    void* ptr = &heap[heap_ptr];
    heap_ptr += 4096;
    memset(ptr, 0, 4096);
    return ptr;
}

