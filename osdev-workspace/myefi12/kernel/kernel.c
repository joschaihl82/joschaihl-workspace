/*
 * kernel.c
 *
 * Freestanding kernel entry (ELF). No UEFI calls or headers.
 * - Built as an ELF (System V AMD64 ABI).
 * - Loader (EFI app) must:
 *     * load ELF PT_LOAD segments into memory,
 *     * set up a stack for the kernel,
 *     * call the kernel entry (symbol: kmain) using System V ABI,
 *       passing a pointer to KernelGOPInfo as the first argument (RDI).
 *
 * Minimal, self-contained framebuffer console:
 * - 8x8 font scaled 2x
 * - k_printf with %s, %x, %c
 * - simple scrolling
 *
 * Compile with an ELF cross toolchain, e.g.:
 *   x86_64-elf-gcc -ffreestanding -nostdlib -O2 -c kernel.c -o kernel.o
 * Link with a simple linker script to set entry to kmain or ensure e_entry points to kmain.
 */

